    private void fetchDataFromMongoDB() {
        // Connect to MongoDB and get the collection
        MongoCollection<Document> collection = Conn1.getCollection("student");

        // Query MongoDB for data
        FindIterable<Document> iterable = collection.find();

        // Create a list to store data
        List<Object[]> data = new ArrayList<>();

        // Extract data from MongoDB documents
        for (Document document : iterable) {
            Object[] rowData = new Object[8];
            rowData[0] = document.getString("Name");
            rowData[1] = document.getString("Father Name");
            rowData[2] = document.getString("CNIC");
            rowData[3] = document.getString("Phone Number");
            rowData[4] = document.getString("Department");
            rowData[5] = document.getString("Registration Number");
            rowData[6] = document.getString("Password");
            rowData[7] = document.getString("Gender");

            data.add(rowData);
        }

        // Create a table model and set data
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Name", "Father Name", "CNIC", "Phone Number", "Department", "Registration Number", "Password", "Gender"});

        for (Object[] row : data) {
            model.addRow(row);
        }

        // Set the model to the JTable
        jTable1.setModel(model);
    }
