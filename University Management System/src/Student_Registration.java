    private void updateActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        // Get the selected row index
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex == -1) {
            // No row selected, show an error message or handle accordingly
            return;
        }

        // Retrieve data from the selected row
        String studentName = name.getText();
        String fatherName = Fathername.getText();
        String cnicNumber = cnic.getText();
        String phoneNumber = phonenumber.getText();
        String department = departement.getText();
        String regNumber = RegistrationNumber.getText();
        String password = Password.getText();
        String gender = male.isSelected() ? "Male" : "Female";

        // Update the corresponding Document in MongoDB
        MongoCollection<Document> collection = Conn1.getCollection("student");
        Document query = new Document("Registration Number", jTable1.getValueAt(selectedRowIndex, 5).toString());
        Document update = new Document("$set", new Document()
                .append("Name", studentName)
                .append("Father Name", fatherName)
                .append("CNIC", cnicNumber)
                .append("Phone Number", phoneNumber)
                .append("Department", department)
                .append("Registration Number", regNumber)
                .append("Password", password)
                .append("Gender", gender));

        collection.updateOne(query, update);

        // Fetch data from MongoDB and update the table
        fetchDataFromMongoDB();
    }

